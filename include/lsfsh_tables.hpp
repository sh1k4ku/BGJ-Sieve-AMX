// for alpha < 0.4, the output is 
// interploated from the table
double lsh_C(long dim, double alpha) {
    #if 1
    return pow(alpha, dim) * 0.33;
    #else
    if (dim != 24) {
        fprintf(stderr, "[Error] lsh_C: dim = %ld is not supported, 0.0 returned\n", dim);
        return 0.0;
    }
    if (alpha < 0.4) {
        return pow(alpha * 2.0, 24.0) * lsh_C(dim, 0.5);
    }
    long alpha_left = (long) floor(alpha * 100.0) - 40;
    long alpha_right = (long) ceil(alpha * 100.0) - 40;
    if (alpha_left < 0) alpha_left = 0;
    if (alpha_right > 60) alpha_right = 60;
    return _lsh_C24[alpha_left] + (100.0 * alpha - alpha_left - 40) * (_lsh_C24[alpha_right] - _lsh_C24[alpha_left]);
    #endif
}

// for alpha < 0.6, and delta < 0.31 or > 0.7
// the output is 0.0 and a warning message will 
// be generated
double lsh_W(long dim, double alpha, double delta) {
    if (dim != 24) {
        fprintf(stderr, "[Error] lsh_W: dim = %ld is not supported, 0.0 returned\n", dim);
        return 0.0;
    }
    if (alpha < 0.6) {
        fprintf(stderr, "[Error] lsh_W: alpha(%lf) < 0.6 is not supported, 0.0 returned\n", alpha);
        return 0.0;
    }
    if (delta < 0.31 || delta > 0.7) {
        //fprintf(stderr, "[Error] lsh_W: delta(%lf) < 0.31 or > 0.7 is not supported, 0.0 returned\n", delta);
        return 0.0;
    }
    long alpha_left = (long) floor(alpha * 100.0) - 60;
    long alpha_right = (long) ceil(alpha * 100.0) - 60;
    if (alpha_left < 0) alpha_left = 0;
    if (alpha_right > 40) alpha_right = 40;
    long delta_left = (long) floor(delta * 100.0) - 31;
    long delta_right = (long) ceil(delta * 100.0) - 31;
    if (delta_left < 0) delta_left = 0;
    if (delta_right > 39) delta_right = 39;
    double delta_left_ret = _lsh_W24[alpha_left][delta_left] + (100.0 * alpha - alpha_left - 60) * (_lsh_W24[alpha_right][delta_left] - _lsh_W24[alpha_left][delta_left]);
    double delta_right_ret = _lsh_W24[alpha_left][delta_right] + (100.0 * alpha - alpha_left - 60) * (_lsh_W24[alpha_right][delta_right] - _lsh_W24[alpha_left][delta_right]);
    return delta_left_ret + (100.0 * delta - delta_left - 31) * (delta_right_ret - delta_left_ret);
}

